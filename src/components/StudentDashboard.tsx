import { X, BarChart2 } from 'lucide-react';
import { useApp } from '../store/context';
import { QUIZ_LEVELS, DIFFICULTY_CONFIG } from '../data/quizLevels';

const TOTAL_QUESTIONS = 30;

function useIsMobile() {
  if (typeof window === 'undefined') return false;
  return window.innerWidth < 768;
}

const diffColors: Record<string, { bg: string; text: string; border: string }> = {
  easy: { bg: '#f0fdf4', text: '#16a34a', border: '#bbf7d0' },
  medium: { bg: '#fffbeb', text: '#d97706', border: '#fde68a' },
  hard: { bg: '#fef2f2', text: '#dc2626', border: '#fecaca' },
};

export function StudentDashboard() {
  const { state, dispatch } = useApp();
  const isMobile = useIsMobile();
  if (!state.showTeacherDash) return null;

  const completed = state.completedChallenges.length;
  const totalScore = state.score;
  const wrong = state.attempts - completed;
  const pct = TOTAL_QUESTIONS > 0 ? Math.round((completed / TOTAL_QUESTIONS) * 100) : 0;

  const exportReport = () => {
    const now = new Date();
    const dateStr = now.toLocaleDateString('en-US', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' });
    const timeStr = now.toLocaleTimeString('en-US', { hour: '2-digit', minute: '2-digit' });

    const correctList = QUIZ_LEVELS
      .filter(q => state.completedChallenges.includes(q.level))
      .map(q => `  Q${q.level} - ${q.description} [${q.difficulty.toUpperCase()}]`)
      .join('\n') || '  None completed yet';

    const wrongList = QUIZ_LEVELS
      .filter(q => !state.completedChallenges.includes(q.level))
      .map(q => `  Q${q.level} - ${q.description} [${q.difficulty.toUpperCase()}]`)
      .join('\n') || '  All questions answered correctly!';

    const report = `
SCILAB 360 - STUDENT PROGRESS REPORT
=====================================

Report generated: ${dateStr} at ${timeStr}

SCORE SUMMARY
-------------
  Points Earned     ${totalScore} pts
  Score             ${completed} / ${TOTAL_QUESTIONS}
  Questions Correct ${completed}
  Questions Wrong   ${wrong > 0 ? wrong : 0}

QUESTIONS ANSWERED CORRECTLY
----------------------------
${correctList}

QUESTIONS NOT YET COMPLETED
----------------------------
${wrongList}

Generated by SciLab 360
`.trim();

    const blob = new Blob(['\uFEFF' + report], { type: 'text/plain;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `scilab360-student-report-${now.toISOString().slice(0, 10)}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div
      style={{
        position: 'fixed',
        top: 0, right: 0, bottom: 0,
        width: isMobile ? '100%' : 340,
        display: 'flex', flexDirection: 'column',
        zIndex: 1000,
        background: '#ffffff',
        borderLeft: isMobile ? 'none' : '1px solid #e2e8f0',
        boxShadow: '-4px 0 24px rgba(0,0,0,0.08)',
        overflowY: 'auto',
      }}
    >
      {/* Header */}
      <div style={{ padding: '18px 20px 16px', display: 'flex', justifyContent: 'space-between', alignItems: 'center', borderBottom: '1px solid #e2e8f0', flexShrink: 0, background: '#f8fafc' }}>
        <h2 style={{ fontFamily: '"Nunito", sans-serif', fontWeight: 800, fontSize: 16, color: '#0f172a', letterSpacing: '-0.01em', margin: 0 }}>
          My Progress
        </h2>
        <button onClick={() => dispatch({ type: 'TOGGLE_TEACHER' })}
          style={{ background: 'transparent', border: 'none', cursor: 'pointer', color: '#94a3b8', fontSize: 18 }}
          onMouseEnter={e => { (e.currentTarget as HTMLButtonElement).style.color = '#475569'; }}
          onMouseLeave={e => { (e.currentTarget as HTMLButtonElement).style.color = '#94a3b8'; }}>
          <X size={20} />
        </button>
      </div>

      <div style={{ display: 'flex', flexDirection: 'column', gap: 14, padding: 18, flex: 1 }}>

        {/* Score banner */}
        <div style={{ borderRadius: 16, padding: 20, background: 'linear-gradient(135deg, #f0fdfa, #ecfdf5)', border: '1px solid #99f6e4', textAlign: 'center' }}>
          <div style={{ fontFamily: '"Nunito", sans-serif', fontSize: 48, fontWeight: 900, color: '#0f766e', lineHeight: 1 }}>
            {totalScore}
          </div>
          <div style={{ fontFamily: '"Inter", sans-serif', fontSize: 12, color: '#5eead4', marginTop: 4, fontWeight: 600, textTransform: 'uppercase', letterSpacing: '0.05em' }}>Total Points</div>

          <div style={{ marginTop: 16, display: 'flex', justifyContent: 'space-between', alignItems: 'center', background: '#ffffff', borderRadius: 12, padding: '12px 14px', border: '1px solid #e2e8f0' }}>
            <div style={{ textAlign: 'center', flex: 1 }}>
              <div style={{ fontFamily: '"Nunito", sans-serif', fontSize: 24, fontWeight: 900, color: '#16a34a' }}>{completed}</div>
              <div style={{ fontFamily: '"Inter", sans-serif', fontSize: 10, color: '#86efac', marginTop: 2, fontWeight: 600, textTransform: 'uppercase' }}>Correct</div>
            </div>
            <div style={{ width: 1, height: 36, background: '#e2e8f0' }} />
            <div style={{ textAlign: 'center', flex: 1 }}>
              <div style={{ fontFamily: '"Nunito", sans-serif', fontSize: 24, fontWeight: 900, color: '#ef4444' }}>{Math.max(0, wrong)}</div>
              <div style={{ fontFamily: '"Inter", sans-serif', fontSize: 10, color: '#fca5a5', marginTop: 2, fontWeight: 600, textTransform: 'uppercase' }}>Wrong</div>
            </div>
            <div style={{ width: 1, height: 36, background: '#e2e8f0' }} />
            <div style={{ textAlign: 'center', flex: 1 }}>
              <div style={{ fontFamily: '"Nunito", sans-serif', fontSize: 24, fontWeight: 900, color: '#0f766e' }}>{completed}<span style={{ fontSize: 14, color: '#94a3b8' }}>/{TOTAL_QUESTIONS}</span></div>
              <div style={{ fontFamily: '"Inter", sans-serif', fontSize: 10, color: '#5eead4', marginTop: 2, fontWeight: 600, textTransform: 'uppercase' }}>Score</div>
            </div>
          </div>

          <div style={{ marginTop: 14, height: 8, background: '#e2e8f0', borderRadius: 999, overflow: 'hidden' }}>
            <div style={{ height: '100%', width: `${pct}%`, background: 'linear-gradient(90deg, #14b8a6, #06b6d4)', borderRadius: 999, transition: 'width 0.5s' }} />
          </div>
          <div style={{ fontFamily: '"Inter", sans-serif', fontSize: 12, color: '#64748b', marginTop: 8, fontWeight: 500 }}>
            {pct}% complete
          </div>
        </div>

        {/* Progress by difficulty */}
        <div style={{ borderRadius: 14, padding: '14px 18px', background: '#f8fafc', border: '1px solid #e2e8f0' }}>
          <p style={{ fontFamily: '"Nunito", sans-serif', fontSize: 11, fontWeight: 800, color: '#0f766e', letterSpacing: '0.05em', marginBottom: 12, textTransform: 'uppercase' }}>Progress by Difficulty</p>
          <div style={{ display: 'flex', flexDirection: 'column', gap: 10 }}>
            {(['easy', 'medium', 'hard'] as const).map(d => {
              const cfg = DIFFICULTY_CONFIG[d];
              const dc = diffColors[d];
              const levelsInD = QUIZ_LEVELS.filter(q => q.difficulty === d);
              const doneInD = levelsInD.filter(q => state.completedChallenges.includes(q.level)).length;
              const dpct = levelsInD.length > 0 ? (doneInD / levelsInD.length) * 100 : 0;
              return (
                <div key={d}>
                  <div style={{ display: 'flex', justifyContent: 'space-between', marginBottom: 5 }}>
                    <span style={{ fontFamily: '"Nunito", sans-serif', fontSize: 13, color: dc.text, fontWeight: 700 }}>{cfg.emoji} {cfg.label}</span>
                    <span style={{ fontFamily: '"Space Mono", monospace', fontSize: 12, color: '#64748b' }}>{doneInD}/{levelsInD.length}</span>
                  </div>
                  <div style={{ height: 6, background: '#e2e8f0', borderRadius: 999, overflow: 'hidden' }}>
                    <div style={{ height: '100%', width: `${dpct}%`, background: dc.text, borderRadius: 999, transition: 'width 0.5s' }} />
                  </div>
                </div>
              );
            })}
          </div>
        </div>

        {/* Student progress list */}
        {(['easy', 'medium', 'hard'] as const).map(d => {
          const cfg = DIFFICULTY_CONFIG[d];
          const dc = diffColors[d];
          const levelsInD = QUIZ_LEVELS.filter(q => q.difficulty === d);
          return (
            <div key={d} style={{ borderRadius: 14, padding: '14px 18px', background: '#ffffff', border: `1px solid ${dc.border}` }}>
              <p style={{ fontFamily: '"Nunito", sans-serif', fontSize: 11, fontWeight: 800, color: dc.text, letterSpacing: '0.05em', marginBottom: 10, textTransform: 'uppercase' }}>
                {cfg.emoji} {cfg.label} - Levels {levelsInD[0].level}-{levelsInD[levelsInD.length - 1].level}
              </p>
              <div style={{ display: 'flex', flexDirection: 'column', gap: 5 }}>
                {levelsInD.map(q => {
                  const done = state.completedChallenges.includes(q.level);
                  return (
                    <div key={q.level} style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', padding: '7px 10px', borderRadius: 8, background: done ? dc.bg : '#f8fafc', border: `1px solid ${done ? dc.border : '#f1f5f9'}` }}>
                      <div style={{ display: 'flex', gap: 8, alignItems: 'center' }}>
                        <span style={{ fontFamily: '"Space Mono", monospace', fontSize: 11, color: '#94a3b8', minWidth: 24 }}>L{q.level}</span>
                        <span style={{ fontFamily: '"Inter", sans-serif', fontSize: 12, color: done ? '#475569' : '#94a3b8' }}>{q.description}</span>
                      </div>
                      <span style={{
                        fontSize: 13,
                        fontWeight: 700,
                        color: done ? dc.text : '#e2e8f0',
                      }}>
                        {done ? '\u2713' : '\u25CB'}
                      </span>
                    </div>
                  );
                })}
              </div>
            </div>
          );
        })}

        {/* Download button */}
        <button onClick={exportReport}
          style={{ display: 'flex', alignItems: 'center', justifyContent: 'center', gap: 8, padding: '14px 16px', borderRadius: 12, cursor: 'pointer', fontFamily: '"Nunito", sans-serif', fontWeight: 800, fontSize: 14, color: '#ffffff', background: '#14b8a6', border: 'none', transition: 'all 0.2s' }}
          onMouseEnter={e => { e.currentTarget.style.background = '#0d9488'; e.currentTarget.style.transform = 'scale(1.02)'; }}
          onMouseLeave={e => { e.currentTarget.style.background = '#14b8a6'; e.currentTarget.style.transform = 'scale(1)'; }}>
          <BarChart2 size={16} />
          Download My Report
        </button>
      </div>
    </div>
  );
}
